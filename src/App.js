function Search({ query, setQuery }) {
  const inputEl = useRef(null); //when working with dom elements

  //Do not do this, instead use useRef for selecting DOM elements!
  // useEffect(function () {
  //   const el = document.querySelector(".search");
  //   console.log(el);
  //   el.focus();
  // }, []);

  useKey("Enter", function () {
    if (document.activeElement === inputEl.current) return; // so that when search input is already focused nothing happens
    //inputEl.current is now the selected DOM element, where we want to call the focus method on
    // console.log(inputEl.current);
    inputEl.current.focus();
    setQuery("");
  });

  return (
    <input
      className="search"
      type="text"
      placeholder="Search movies..."
      value={query}
      onChange={(e) => setQuery(e.target.value)}
      ref={inputEl} // for the created ref above!
    />
  );
}

